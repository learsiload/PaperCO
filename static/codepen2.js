
var tag2_in = document.getElementById("tag2_in");
var tag2_out = document.getElementById("tag2_out");

tag2_in.addEventListener("input", showSliderValue1, false);

function showSliderValue1() {
  tag2_out.innerHTML = tag2_in.value;
  var bulletPosition = (tag2_in.value /tag2_in.max);
  tag2_out.style.left = (bulletPosition * 578) + "px";
}


